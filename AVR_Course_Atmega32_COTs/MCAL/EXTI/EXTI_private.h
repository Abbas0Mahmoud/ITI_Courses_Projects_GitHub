/*                   Interrupts Control Sense Registers/Bits                             */
/*_______________________________________________________________________________________*/

#define     MCUCR            *((volatile u8 *)0x55)     // MCU Control Register For INT0 / INT1 Interrupt Sense Control
#define     MCUCR_ISC00      0                          // Interrupt 0 Sense Control Bit0
#define     MCUCR_ISC01      1                          // Interrupt 0 Sense Control Bit1
#define     MCUCR_ISC02      2                          // Interrupt 1 Sense Control Bit2
#define     MCUCR_ISC03      3                          // Interrupt 1 Sense Control Bit3

#define     MCUCSR           *((volatile u8 *)0x54)     // MCU Control and Status  Register For INT2 Interrupt Sense Control
#define     MCUCSR_ISC2      6                          // Interrupt 2 Sense Control Bit

/*_______________________________________________________________________________________*/

/*          General Interrupt Control Register (PIE)                                     */
/*_______________________________________________________________________________________*/

#define     GICR             *((volatile u8 *)0x5B)     // Global Interrupt Control register
#define     GICR_INT2        5                          // Interrupt 2 Enable (PIE)
#define     GICR_INT0        6                          // Interrupt 0 Enable (PIE)
#define     GICR_INT1        7                          // Interrupt 1 Enable (PIE)

/*_______________________________________________________________________________________*/

/*          Global Interrupt Flag Register (PIF)                                         */
/*_______________________________________________________________________________________*/

#define     GIFR             *((volatile u8 *)0x5A)     // Global Interrupt Falg Register
#define     GIDR_INTF2       5                          // Interrupt 2 Flag (PIF)
#define     GIDR_INTF0       6                          // Interrupt 0 Flag (PIF)
#define     GIDR_INTF1       7                          // Interrupt 1 Flag (PIF)

